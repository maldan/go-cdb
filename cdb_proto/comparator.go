package cdb_proto

import "unsafe"

func StrCmp(a []byte, b []byte) bool {
	if len(a) != len(b) {
		return false
	}
	for i := 0; i < len(a); i++ {
		if a[i] != b[i] {
			return false
		}
	}
	return true
}

func StrCmpR(ar unsafe.Pointer, br unsafe.Pointer) bool {
	a := *(*[]byte)(ar)
	b := *(*[]byte)(br)
	if len(a) != len(b) {
		return false
	}
	for i := 0; i < len(a); i++ {
		if a[i] != b[i] {
			return false
		}
	}
	return true
}
